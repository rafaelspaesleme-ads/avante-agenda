    public void CarregarListNota() {

        String titulo = Lista.getSelectedValue();

        try {

            if (!titulo.equals("")) {

                String banco = "jdbc:mysql://127.0.0.1:3306/agendatecnologica";
                String usuario = "root";
                String senha = "";
                String nomedodriver = "com.mysql.jdbc.Driver";
                Class.forName(nomedodriver).newInstance();
                Connection con = DriverManager.getConnection(banco, usuario, senha);

                // comando sql inserção no banco
                String sql = "SELECT * FROM tb_nota WHERE titulo = ?";

                //Cria o comando
                PreparedStatement comando = con.prepareStatement(sql);

                comando.setString(1, titulo);

                ResultSet rs;
                rs = comando.executeQuery();

                while (rs.next()) {

                    txtTitulo.setText(rs.getString("titulo"));

                    Date g = rs.getDate("data");

                    SimpleDateFormat f = new SimpleDateFormat("dd-MM-yyyy");
                    txtData.setText(f.format(g));

                    txtNome.setText(rs.getString("nome"));

                    txtDescricao.setText(rs.getString("descricao"));

                }

            } else {
                JOptionPane.showMessageDialog(null, "Selecione um Contato!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }

    }

