    public void ExibirAnotacao() {

        try {

            String banco = "jdbc:mysql://127.0.0.1:3306/agendatecnologica";
            String usuario = "root";
            String senha = "";
            String nomedodriver = "com.mysql.jdbc.Driver";
            Class.forName(nomedodriver).newInstance();
            Connection conn = DriverManager.getConnection(banco, usuario, senha);

            String sql = "SELECT * FROM tb_nota";

            PreparedStatement comando = conn.prepareStatement(sql);
            ResultSet rs = comando.executeQuery();

            cbxNota.removeAllItems();

            ArrayList<String> lista = new ArrayList<String>();

            while (rs.next()) {
                lista.add(rs.getString("titulo"));
                cbxNota.addItem(rs.getString("titulo"));

            }

            Iterator it = lista.iterator();
            DefaultListModel model = new DefaultListModel();
            Lista.setModel(model);
            while (it.hasNext()) {
                String itemLista = (String) it.next();
                model.addElement(itemLista);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }

